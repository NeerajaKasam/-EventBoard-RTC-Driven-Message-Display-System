#ifndef _TYPES_H
#define _TYPES_H

/* Unsigned and signed 8-bit data types */
typedef unsigned char u8;
typedef char s8;

/* Unsigned and signed 16-bit data types */
typedef unsigned short u16;
typedef short s16;

/* Unsigned and signed 32-bit data types */
typedef unsigned int u32;
typedef int s32;

/* Floating-point data types */
typedef float f32;
typedef double f64;

#endif